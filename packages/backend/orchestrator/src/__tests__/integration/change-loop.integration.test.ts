function spawnGit(args: string[], cwd: string): Promise<{ exitCode: number; stdout: string; stderr: string }> {
  return new Promise((resolve, reject) => {
    const proc = spawn('git', args, { cwd })
    let stdout = ''
    let stderr = ''
    proc.stdout.on('data', (d: Buffer) => { stdout += d.toString() })
    proc.stderr.on('data', (d: Buffer) => { stderr += d.toString() })
    proc.on('close', code => resolve({ exitCode: code ?? 1, stdout, stderr }))
    proc.on('error', reject)
  })
}

async function createTempGitRepo(): Promise<{
  repoDir: string
  cleanup: () => Promise<void>
}> {
  const repoDir = await fs.mkdtemp(path.join(os.tmpdir(), 'cl-test-repo-'))

  await spawnGit(['init', '--initial-branch=main'], repoDir)
    .catch(() => spawnGit(['init'], repoDir))

  await spawnGit(['config', 'user.email', 'test@test.com'], repoDir)
  await spawnGit(['config', 'user.name', 'Test User'], repoDir)

  // Initial commit
  await fs.writeFile(path.join(repoDir, 'README.md'), '# Test\n', 'utf-8')
  await spawnGit(['add', '.'], repoDir)
  await spawnGit(['commit', '-m', 'Initial commit'], repoDir)

  return {
    repoDir,
    cleanup: async () => {
      try { await fs.rm(repoDir, { recursive: true, force: true }) } catch { /* ignore */ }
    },
  }
}

// ============================================================================
// Mock IModelDispatch that returns a simple file create output
// ============================================================================

function makeSuccessDispatch(output = '// generated by test'): IModelDispatch {
  return {
    dispatch: async () => ({
      success: true,
      output,
      durationMs: 10,
    }),
  }
}

// ============================================================================
// Tests: spawnCommand helper
// ============================================================================

describe(
  'spawnCommand integration: real subprocess',
  () => {
    it(
      'runs echo command and captures stdout',
      async () => {
        const result = await spawnCommand('echo', ['hello integration'], { cwd: os.tmpdir() })
        expect(result.exitCode).toBe(0)
        expect(result.stdout.trim()).toBe('hello integration')
      },
      180_000,
    )

    it(
      'returns non-zero exit code for failing command',
      async () => {
        const result = await spawnCommand('sh', ['-c', 'exit 1'], { cwd: os.tmpdir() })
        expect(result.exitCode).toBe(1)
      },
      180_000,
    )
  },
)

